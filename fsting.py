name = "Alice"
age = 30
city = "Wonderland"

greeting = f"My name is {name}, I am {age} years old, and I live in {city}."
print(greeting)
# Output: My name is Alice, I am 30 years old, and I live in Wonderland.

print(f"Next year, {name} will be {age + 1} years old.")
# Output: Next year, Alice will be 31 years old.

print(f"My name in uppercase is {name.upper()}.")
# Output: My name in uppercase is ALICE.
